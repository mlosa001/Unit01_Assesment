package org.pursuit;

public class PrimitiveConverter {



    private int intToInteger(int n) {
        return n;
    }

    private double doubleToDouble(double n) {
        return n;
    }

    private char charToCharacter(char n) {
        return n;
    }

    private boolean booleanToBoolean(boolean b) {
        return b;
    }

}
